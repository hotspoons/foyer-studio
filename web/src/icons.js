// Path → full SVG string (lazy).
function svgFor(name) {
  if (ALIASES[name]) name = ALIASES[name];
  const p = PATHS[name];
  if (!p) return null;
  return outline(p);
}

// Public: a dict of ready-to-embed SVG strings, keyed by Heroicons name.
export const ICONS = new Proxy(
  {},
  {
    get(_t, name) {
      return svgFor(String(name));
    },
  }
);

/**
 * Render a named icon as a Lit template, sized by `size` (px).
 * Usage: `${icon("play", 16)}`
 */
export function icon(name, size = 16) {
  const raw = svgFor(name);
  if (!raw) return html`<span style="display:inline-block;width:${size}px;height:${size}px"></span>`;
  const sized = raw.replace("<svg ", `<svg width="${size}" height="${size}" `);
  return html`<span
    style="display:inline-flex;align-items:center;justify-content:center;width:${size}px;height:${size}px"
    >${unsafeHTML(sized)}</span
  >`;
}

